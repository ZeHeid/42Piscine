#include <unistd.h>

void	ft_print_combo2(void)
{
	char	tab[5];
	int		a;
	int		b;

	a = 0;
	b = 1;
	tab[2] = 32;
	while (a <= 98)
	{
		tab[0] = a / 10 + 48;
		tab[1] = a % 10 + 48;
		while (b <= 99)
		{
			tab[3] = b / 10 + 48;
			tab[4] = b % 10 + 48;
			write(1, &tab, 5);
			if (a != 98)
				write(1, ", ", 2);
			b++;
		}
		a++;
		b = a + 1;
	}
}
