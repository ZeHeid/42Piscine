int	ft_str_is_uppercase(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			j++;
		i++;
	}
	if (j == i)
		return (1);
	return (0);
}
